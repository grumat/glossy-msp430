/*!
Set JTCLK (= JTDI = SPI MOSI) high.

In dtrig mode, SPI SCK (PA5 = JTCK) cannot be gated independently, so sending a byte
causes 8 JTCK pulses.  This is harmless in Run-Test/Idle (TMS=0 after the last frame).
The MSB-first 0xFF byte leaves MOSI (= JTCLK) HIGH after the last bit.
*/
void JtagDev::OnSetTclk()
{
	AcquireSpiClkMuted();
	SpiJtagDev::PutChar(0xff);
}


void JtagDev::OnClearTclk()
{
	AcquireSpiClkMuted();
	SpiJtagDev::PutChar(0x00);
}


/*!
One TCLK rising transition (low → high).

0xF0 = 0b11110000: MOSI high for first 4 bits then low for last 4.
The falling edge of JTCK (bit 4) with MOSI=0 followed by MOSI=1 at bit 5 is the
rising edge of JTCLK seen by the MSP430.  MOSI ends HIGH after the byte.
*/
void JtagDev::OnPulseTclk()
{
	AcquireSpiClkMuted();
	SpiJtagDev::PutChar(0xf0);
}


/*!
One TCLK falling transition (high → low → high).

0x0F = 0b00001111: MOSI low for first 4 bits, high for last 4.
MOSI ends HIGH after the byte.
*/
void JtagDev::OnPulseTclkN()
{
	AcquireSpiClkMuted();
	SpiJtagDev::PutChar(0x0f);
}


/*!
Generate high-frequency JTCLK pulses for MSP430 flash operations (~450 kHz).

PA7 stays in SPI1_MOSI alt-function the entire time. The TCLK burst is produced
by streaming a precomputed bit pattern (gJtmsWave) at a reduced SPI baud
(kJtclkSpiClock); JTCK (PA5) is parked as GPIO-high during the burst by
MuteSpiClk so it doesn't toggle along with MOSI.
*/
void JtagDev::OnFlashTclk(uint32_t min_pulses)
{
	JtagWaitTransfer();			// SpiJtmsWave reconfigures SPI1 — must not race a shift DMA
	JTEST::SetLow();
	AcquireSpiClkMuted();		// PA7 stays in SPI1_MOSI AF; PA5 parked GPIO-high
	SpiJtmsWave::Disable();
	Spi::RawSpiSpeed oldspeed = SpiJtmsWave::SetupSpeed();
	SpiJtmsWave::Enable();
	for (uint32_t pulses = 0; pulses < min_pulses; pulses += kNumPeriods)
		SpiJtmsWave::PutStream(gJtmsWave, _countof(gJtmsWave));
	SpiJtmsWave::DisableSafe();
	SpiJtmsWave::RestoreSpeed(oldspeed);
	SpiJtmsWave::Enable();		// DtrigJtag::Start() does not set SPE; restore it here
}

