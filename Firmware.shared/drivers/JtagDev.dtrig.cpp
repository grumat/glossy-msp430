JtagDev::JtagDev()   {}


// ── JtagDev virtual method implementations ────────────────────────────────────

bool JtagDev::OnOpen()
{
	/*
	Workflow: Open -> ConnectJtag -> EnterTap -> ResetTap -> JTAG mode ready
			  \__/
	*/
	s_hw_mode = HwMode::kOff;
	s_have_in_flight_ = false;	// fresh start: no DMA can be live yet
	DtrigDr32::ReleaseDma();  // clear stale DMA1_CH2/CH3 EN bits before SPI init
	s_cnt_offset = kDtrigCntOffset_1;
	DtrigInit_1::Init();
	// Ensure MOSI settles high required for fuse check
	AcquireTmsPwm();

#if OPT_TEST_WITH_LOGIC_ANALYZER_
	DoLogicAnalyzerTest();
#endif

	return true;
}


void JtagDev::OnClose()
{
	JtagWaitTransfer();		// don't tear down the SPI mid-DMA
	OnReleaseDriver();		// Init state: hardware buffers to Hi-Z, BusState::kStandby
	// Disable() clears SPE only; the SPI1 RCC clock stays on. The clock is owned
	// by the platform PeripheralEnabler (enabled once at boot) and SpiDevice::Setup()
	// — re-run by the next OnOpen() — assumes it is live: register writes (incl. SPE)
	// are dropped while the peripheral clock is gated. Stop() would gate it and
	// nothing re-enables it, so a second jtag_scan would come up with SPE=0 and hang
	// in PutChar() waiting for TXE.
	SpiJtagDev::Disable();
	s_hw_mode = HwMode::kOff;
}


/*!
Switch PA5/PA6/PA7 to SPI AF mode and assert JTAG bus active.
*/
void JtagDev::OnConnectJtag(BusSpeed speed)
{
	/*
	Workflow: Open -> ConnectJtag -> EnterTap -> ResetTap -> JTAG mode ready
	                  \_________/
	*/

	speed_ = speed;

	// slau320: ConnectJTAG / DrvSignals. JtagOn brings RST HIGH (JRST_On) and TEST
	// low, matching UIF's pre-entry state (no RST-low pre-pulse) — OnEnterTap then
	// starts from RST=H like TI _hil_Connect, and TEST stays low until the //3
	// activation. See I2031_ACQUISITION_GOLDEN_REFERENCE.md.
	JtagOn::Setup();
	// The acquisition bus state (only the RST buffer live for the TEST/RST entry
	// glitch) and its settle delay are set at the top of OnEnterTap, which always
	// follows this call and is also re-entered directly on the RstLow->RstHigh
	// retry with no intervening OnConnectJtag.
}


void JtagDev::SetSpeed(BusSpeed speed)
{
	switch (speed)
	{
	case BusSpeed::kSlowest: s_cnt_offset = kDtrigCntOffset_1; DtrigInit_1::ApplySpeed(); break;
	case BusSpeed::kSlow:    s_cnt_offset = kDtrigCntOffset_2; DtrigInit_2::ApplySpeed(); break;
	case BusSpeed::kMedium:  s_cnt_offset = kDtrigCntOffset_3; DtrigInit_3::ApplySpeed(); break;
	case BusSpeed::kFast:    s_cnt_offset = kDtrigCntOffset_4; DtrigInit_4::ApplySpeed(); break;
	case BusSpeed::kFastest: s_cnt_offset = kDtrigCntOffset_5; DtrigInit_5::ApplySpeed(); break;
	}
}


void JtagDev::OnReleaseJtag()
{
	// Close state: the buffers DRIVE the bus idle level (no Hi-Z, buffers stay
	// enabled) so weak/asymmetric target pull-ups/-downs cannot pulse the lines
	// between acquisition attempts. Full Hi-Z release is OnReleaseDriver(), called
	// only from OnClose(). slau320 StopJtag preamble: settle MOSI high, drop TMS/TEST.
	AcquireSpiClkMuted();
	SpiJtagDev::PutChar(0xFF);
	AcquireTmsGpio();
	JTMS::SetLow();
	JTEST::SetLow();
	// Buffers remain enabled; drive the JTAG idle outputs (RST=H, TEST=L, TMS=L,
	// TCK=H, TDI=H; TDO stays input). JtagOff is now the *driven* idle group, so
	// keep the full JTAG bus enabled (kJtag) — not Hi-Z — between attempts.
	SetBusState(BusState::kJtag);
	JtagOff::Setup();
	s_hw_mode = HwMode::kOff;
	StopWatch().Delay<Msec(10)>();
}


void JtagDev::OnReleaseDriver()
{
	// Init state: release the hardware buffers to Hi-Z so the target's own
	// pull-ups/-downs own the bus. Only full electrical release; not used inside
	// the acquisition retry loop (that stays in the driven Close state).
	SetBusState(BusState::kStandby);
	DriverOff::SetupPinMode();
	s_hw_mode = HwMode::kOff;
}


/*!
Reset the JTAG TAP and perform fuse-HW check.

Uses DtrigGoIdle (SPI + TIM1_CH2N) to clock 6× TMS=1 + 1× TMS=0 for a guaranteed
Test-Logic-Reset from any JTAG state, then bit-bangs the fuse-check TMS pulses.
After DoGoIdle(), PB14 is in TIM1_CH2N AF mode; AcquireTmsGpio() restores GPIO mode
so the subsequent bit-bang operations work correctly.
*/
void JtagDev::OnResetTap()
{
	/*
	Workflow: Open -> ConnectJtag -> EnterTap -> ResetTap -> JTAG mode ready
												 \______/
	*/

	JtagWaitTransfer();		// drain any leftover async shift before TAP reset
	AcquireSpiMode();

	// Ensure TMS is HIGH as GPIO before Start() switches PB14 to TIM1_CH2N AF
	//JTMS::SetHigh();
	//JTMS::SetupPinMode();

	DtrigGoIdle::DoGoIdle(buf.GetCurrent1(), kDtrigCntOffset_R);

	// DoGoIdle() leaves PB14 in TIM1_CH2N AF mode (TMS=LOW=RTI).
	// Restore GPIO control for the fuse-check bit-bang pulses.
	JTMS::SetHigh();
	AcquireTmsGpio();

	// Fuse-check TMS pulses (slau320 §2.3.1.2): exactly TWO TMS pulses, each with a
	// low phase ≥5 µs (current into TDI/TEST settles during the low phase). TMS rests
	// HIGH; the two negative pulses are the fuse check. No TCK is clocked here, so the
	// TAP stays in Run-Test/Idle (the pulses only drive the analog fuse-sense).
	StopWatch().Delay<Usec(1)>();
	JTMS::SetLow();
	StopWatch().Delay<Usec(6)>();	// fuse pulse 1 (low ≥5 µs)
	JTMS::SetHigh();
	StopWatch().Delay<Usec(1)>();
	JTMS::SetLow();
	StopWatch().Delay<Usec(6)>();	// fuse pulse 2 (low ≥5 µs)

	// Pre-arm the TMS timer output to its idle-HIGH state BEFORE handing the pin to
	// TIM AF. The preceding GoIdle frame froze OCREF LOW (its last toggle was TMS→RTI)
	// and the timer is stopped, so a bare GPIO→AF switch would surface that stale LOW
	// and drop TMS until the next frame's Start() re-forces it — a spurious third low
	// pulse of variable width on the LA. kForceActive/kForceInactive update OCREF
	// immediately even with the timer stopped, so the AF takeover stays HIGH.
	DtrigGoIdle::TmsOut::SetOutputMode(DtrigGoIdle::kIdleOutMode_);
	AcquireTmsPwm();

	// GoIdle ran at grade-1 (slowest); ramp up to the requested speed now that
	// the TAP is in Run-Test/Idle and subsequent frames need full clock rate.
	SetSpeed(speed_);
}


/// Drains the most recently issued shift's DMA, if one is still in flight.
/// Called from `JtagPending::operator T()` / `Get()` and from the start of
/// each shift body (to wait on the previous frame). Idempotent: clears the
/// in-flight flag so repeat calls are no-ops.
void JtagWaitTransfer()
{
	if (!s_have_in_flight_)
		return;
	// All shift variants share the same SPI RX DMA channel and the same TIM1
	// auto-stop flag, so any DtrigXxx::Wait() drains the in-flight frame.
	DtrigDr8::Wait();
	s_have_in_flight_ = false;
}


JtagPending<uint8_t> JtagDev::OnIrShift(Ir instruction)
{
	AcquireSpiMode();
	using R = DtrigIr8;
	uint8_t *tx = buf.GetNext1();
	R::RenderTransaction(tx, JTCLK::IsHigh(), E2I(instruction));
	JtagWaitTransfer();					// drain previous frame
	buf.Step();
	uint8_t *rx = buf.GetCurrent2();
	R::Start(tx, rx, s_cnt_offset);		// kick new DMA, return immediately
	s_have_in_flight_ = true;
	return { rx, +[](const uint8_t* p) -> uint8_t { return static_cast<uint8_t>(DtrigIr8::GetResult(p)); } };
}


